<template>
  <div class="login-container">
    <div class="login-box">
      <div class="login-header">
        <h1 class="title">医疗质控系统</h1>
        <p class="subtitle">Medical Quality Control System</p>
      </div>

      <el-form
        ref="loginFormRef"
        :model="loginForm"
        :rules="loginRules"
        class="login-form"
        @submit.prevent="handleLogin"
      >
        <el-form-item prop="username">
          <el-input
            v-model="loginForm.username"
            placeholder="请输入用户名"
            size="large"
            :prefix-icon="User"
            clearable
          />
        </el-form-item>

        <el-form-item prop="password">
          <el-input
            v-model="loginForm.password"
            type="password"
            placeholder="请输入密码"
            size="large"
            :prefix-icon="Lock"
            show-password
            clearable
            @keyup.enter="handleLogin"
          />
        </el-form-item>

        <el-form-item>
          <el-button
            type="primary"
            size="large"
            class="login-button"
            :loading="loading"
            @click="handleLogin"
          >
            {{ loading ? '登录中...' : '登录' }}
          </el-button>
        </el-form-item>
      </el-form>

      <div class="login-footer">
        <p class="tip">提示：当前版本无需验证，直接点击登录即可</p>
      </div>
    </div>
  </div>
</template>

<script setup lang="ts">
import { ref, reactive } from 'vue'
import { useRouter } from 'vue-router'
import { ElMessage, type FormInstance, type FormRules } from 'element-plus'
import { User, Lock } from '@element-plus/icons-vue'

const router = useRouter()
const loginFormRef = ref<FormInstance>()
const loading = ref(false)

const loginForm = reactive({
  username: '',
  password: ''
})

// 表单验证规则（虽然不需要验证，但保留基本规则）
const loginRules: FormRules = {
  username: [
    { required: true, message: '请输入用户名', trigger: 'blur' }
  ],
  password: [
    { required: true, message: '请输入密码', trigger: 'blur' }
  ]
}

const handleLogin = () => {
  loading.value = true
  
  // 模拟登录请求（延迟一下，体验更好）
  setTimeout(() => {
    loading.value = false
    
    // 保存登录状态（可以存到 localStorage 或 pinia store）
    localStorage.setItem('isLoggedIn', 'true')
    localStorage.setItem('username', loginForm.username || '用户')
    
    ElMessage.success('登录成功')
    
    // 跳转到主页面
    router.push('/rule/manage').catch((err) => {
      // 忽略重复导航错误（Vue Router 3.x/4.x 的特性）
      if (err.name !== 'NavigationDuplicated') {
        console.error('路由跳转失败:', err)
      }
    })
  }, 500)
}
</script>

